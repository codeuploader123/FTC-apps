import java.util.Scanner; // import library Scanner to get input from user

public class Main {
  public static void main(String[] args) {
    int n =0;
    for (int i =1 ; i<=10; i++ ) { // keep on looping until i = 10
      n += i; // add i to n
    }
    System.out.println(n); // print the result
  }
}
